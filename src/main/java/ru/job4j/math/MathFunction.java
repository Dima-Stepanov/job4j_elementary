public class MathFunction {
    /**
     * sum
     *
     * @param firs double
     * @param second double
     * @return first + second
     */
    public static double sum(double firs, double second) {
        return firs + second;
    }

    /**
     * multiply
     *
     * @param first double
     * @param second double
     * @return first * second
     */
    public static double multiply(double first, double second) {
        return first * second;
    }

    /**
     * difference
     *
     * @param first double
     * @param second double
     * @return first - second
     */
    public static double difference(double first, double second) {
        return first - second;
    }

    /**
     * divide
     *
     * @param first double
     * @param second double
     * @return first / second
     */
    public static double divide(double first, double second) {
        return first / second;
    }
}
